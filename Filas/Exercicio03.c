/*Em quais situações seriam mais vantajoso usar uma fila sequencial estática ou dinâmica encadeada?*/

/*R: A fila sequencial estática possui seus elementos armazenados sequencialmente na memória, portanto,
caso você já saiba previamente o tamanho que a fila deverá possuir e esse tamanho não for muito grande,
a fila estática será mais vantajosa, caso contrário, a dinâmica encadeada que será.*/